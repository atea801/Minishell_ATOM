int	quote_state(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == '"' && str[i + 1] == 39)
			return (1);
		if (str[i] == 39 && str[i + 1] == '"')
			return (1);
		i++;
	}
	return (0);
}
